// Helper function to get optimized image URLs
export const getOptimizedImageUrl = (
  url: string, 
  width: number = 800, 
  height: number = 600, 
  quality: number = 80
): string => {
  // Extract the base URL and add optimization parameters
  const baseUrl = url.split('?')[0];
  return `${baseUrl}?w=${width}&h=${height}&fit=crop&crop=center&auto=format&q=${quality}`;
};

